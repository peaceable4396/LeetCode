/**
 * 请实现一个函数，把字符串 s 中的每个空格替换成"%20"。
 *
 * 示例 1： 输入：s = "We are happy."
 * 输出："We%20are%20happy."
 * @author sx
 * @since 2022/04/28 09:28
 */

public class ReplaceSpace {
    public static void main(String[] args) {

    }
    public String replaceSpace(String s) {
        return s.replace(" ","%20");
    }
}
